# === ОСНОВНЫЕ КОМАНДЫ GIT ===

#  Инициализация нового репозитория
"git init"   # создать новый репозиторий в текущей папке

#  Клонирование репозитория
"git clone <url>"   # скачать репозиторий с GitHub/GitLab

#  Проверка состояния
"git status"   # показывает изменённые файлы и статус репозитория

#  Добавление файлов в индекс (staging)
"git add <file>"     # добавить конкретный файл
"git add ."          # добавить все изменения

#  Создание коммита
"git commit -m 'Сообщение'"   # зафиксировать изменения с сообщением
"git commit"                  # откроет редактор для ввода сообщения

#  История коммитов
"git log"          # полный журнал коммитов
"git log --oneline" # сокращённый журнал (одна строка на коммит)

#  Работа с ветками
"git branch"             # показать список веток
"git branch <name>"      # создать новую ветку
"git checkout <name>"    # переключиться на ветку
"git checkout -b <name>" # создать и сразу переключиться
"git merge <name>"       # слить ветку с текущей

#  Работа с удалённым репозиторием
"git remote -v"                    # список удалённых репозиториев
"git remote add origin <url>"      # связать локальный с GitHub
"git push origin main"             # отправить изменения в GitHub
"git push -u origin main"          # первая отправка (связать ветку)
"git pull origin main"             # получить изменения с GitHub

#  Отмена изменений
"git restore <file>"        # отменить изменения в файле
"git restore --staged <file>" # убрать файл из индекса
"git reset --hard HEAD"     # вернуть всё к последнему коммиту

#  Сравнение изменений
"git diff"        # показать разницу между рабочей папкой и последним коммитом
"git diff --staged" # разница между индексом и последним коммитом

#  Теги
"git tag <name>"            # создать тег
"git tag -a v1.0 -m 'релиз'" # создать аннотированный тег
"git push origin v1.0"      # отправить тег на GitHub

#  Другие полезные
"git config --global user.name 'ТвоёИмя'"   # задать имя
"git config --global user.email 'mail@example.com'" # задать email
